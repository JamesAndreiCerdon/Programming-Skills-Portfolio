"""Chpater 5 Exercise 1: Person 
Use a dictionary to store information about a person you know.Store their first name, last name, age, and the city in which they live. You

should have keys such as first_name, last_name, age, and city. Print each piece of information stored in your dictionary."""

#person is a dictionary with several "key,value" pairs. In this case, person's name is "Ryan Battler," 
#person's age is 22, and their city is "Sharjah." 
#The code will print the first and last names, as well as the person's age and city. 
#It's just that simple, and you can use it as a reference to keep all information of a person in one place!

person = {
    'first_name': 'Ryan',
    'last_name': 'Battler',
    'age': 22,
    'city': 'Sharjah',
    }

print(person['first_name'])
print(person['last_name'])
print(person['age'])
print(person['city'])

   